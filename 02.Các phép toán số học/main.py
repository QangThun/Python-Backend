# Các phép toán số học 
# +, -, *, /, //, %, **

# print(a/b) #chia lấy kết quả thực
# print(a//b) #chia lấy kết quả nguyên  
# print(a%b) #chia lấy phần dư
# print(a**b) #lũy thừa
a = int(input("Nhập vào a: "))
b = int(input("Nhập vào b: "))
print("{0} + {1} = {2}" .format(a ,b , a+b))
print("{0} - {1} = {2}" .format(a ,b , a-b))    
print("{0} * {1} = {2}" .format(a ,b , a*b))    
print("{0} / {1} = {2}" .format(a ,b , a/b))    
print("{0} // {1} = {2}" .format(a ,b , a//b))    
print("{0} % {1} = {2}" .format(a ,b , a%b))    
print("{0} ** {1} = {2}" .format(a ,b , a**b))    
